int	ft_atoi(char *s)
{
	int	neg;
	int	value;

	neg = 0;
	value = 0;
	while (*s == ' ' || *s == '\n' || *s == '\v' || *s == '\t'
			|| *s == '\r' || *s == '\f')
		s++;
	if (*s == '+')
		s++;
	else if (*s == '-')
	{
		neg = 1;
		s++;
	}
	while ((*s <= '9') && (*s >= '0'))
	{
		value = (value * 10) - (*s - '0');
		s++;
	}
	if (!neg)
		value = -value;
	return (value);
}

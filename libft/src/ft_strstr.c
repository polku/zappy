#include <string.h>

char	*ft_strstr(char *s1, char *s2)
{
	int	i;
	int	j;

	i = 0;
	if (s2[0] == '\0')
		return (s1);
	while (s1[i] != '\0')
	{
		j = 0;
		while (s1[i + j] == s2[j])
		{
			if (s2[j + 1] == '\0')
				return (s1 + i);
			j++;
		}
		i++;
	}
	return (NULL);
}
